import 'dart:io';

import 'package:flutter/cupertino.dart';
import 'package:flutter/material.dart';
import 'package:modal_bottom_sheet/modal_bottom_sheet.dart';
import 'package:noty_mobile/features/image_selector/image_selector_screen.dart';

class ImagePickerFeature {
  static Page<File?> openImagePicker() {
    return ImageSelectorPage();
  }
}

class ImageSelectorPage extends Page<File?> {
  @override
  Route<File?> createRoute(BuildContext context) {
    return CupertinoModalBottomSheetRoute<File?>(
      expanded: false,
      settings: this,
      builder: (BuildContext context) {
        return const ImageSelectorScreen();
      },
    );
  }
}
